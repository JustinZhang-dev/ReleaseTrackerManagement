    private static final Long DEFAULT_NOTIFICATION_ID = 1L;
    private static final Long UPDATED_NOTIFICATION_ID = 2L;

    private static final Long DEFAULT_NOTIFY_SEQ = 1L;
    private static final Long UPDATED_NOTIFY_SEQ = 2L;

    private static final String DEFAULT_NOTIFY_TYPE = "AAAAAAAAAA";
    private static final String UPDATED_NOTIFY_TYPE = "BBBBBBBBBB";

    private static final String DEFAULT_NOTIFY_TO = "AAAAAAAAAA";
    private static final String UPDATED_NOTIFY_TO = "BBBBBBBBBB";

    private static final String DEFAULT_NOTIFY_BY = "AAAAAAAAAA";
    private static final String UPDATED_NOTIFY_BY = "BBBBBBBBBB";

    private static final String ENTITY_API_URL = "/api/notifications";
    private static final String ENTITY_API_URL_ID = ENTITY_API_URL + "/{id}";

    private static Random random = new Random();
    private static AtomicLong count = new AtomicLong(random.nextInt() + (2 * Integer.MAX_VALUE));

    @Autowired
    private NotificationRepository notificationRepository;

    @Autowired
    private EntityManager em;

    @Autowired
    private MockMvc restNotificationMockMvc;

    private Notification notification;

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Notification createEntity(EntityManager em) {
        Notification notification = new Notification()
            .notificationId(DEFAULT_NOTIFICATION_ID)
            .notifySeq(DEFAULT_NOTIFY_SEQ)
            .notifyType(DEFAULT_NOTIFY_TYPE)
            .notifyTo(DEFAULT_NOTIFY_TO)
            .notifyBy(DEFAULT_NOTIFY_BY);
        return notification;
    }

    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Notification createUpdatedEntity(EntityManager em) {
        Notification notification = new Notification()
            .notificationId(UPDATED_NOTIFICATION_ID)
            .notifySeq(UPDATED_NOTIFY_SEQ)
            .notifyType(UPDATED_NOTIFY_TYPE)
            .notifyTo(UPDATED_NOTIFY_TO)
            .notifyBy(UPDATED_NOTIFY_BY);
        return notification;
    }

    @BeforeEach
    public void initTest() {
        notification = createEntity(em);
    }

    @Test
    @Transactional
    void createNotification() throws Exception {
        int databaseSizeBeforeCreate = notificationRepository.findAll().size();
        // Create the Notification
        restNotificationMockMvc
            .perform(
                post(ENTITY_API_URL)
                    .with(csrf())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(notification))
            )
            .andExpect(status().isCreated());

        // Validate the Notification in the database
        List<Notification> notificationList = notificationRepository.findAll();
        assertThat(notificationList).hasSize(databaseSizeBeforeCreate + 1);
        Notification testNotification = notificationList.get(notificationList.size() - 1);
        assertThat(testNotification.getNotificationId()).isEqualTo(DEFAULT_NOTIFICATION_ID);
        assertThat(testNotification.getNotifySeq()).isEqualTo(DEFAULT_NOTIFY_SEQ);
        assertThat(testNotification.getNotifyType()).isEqualTo(DEFAULT_NOTIFY_TYPE);
        assertThat(testNotification.getNotifyTo()).isEqualTo(DEFAULT_NOTIFY_TO);
        assertThat(testNotification.getNotifyBy()).isEqualTo(DEFAULT_NOTIFY_BY);
    }

    @Test
    @Transactional
    void createNotificationWithExistingId() throws Exception {
        // Create the Notification with an existing ID
        notification.setId(1L);

        int databaseSizeBeforeCreate = notificationRepository.findAll().size();

        // An entity with an existing ID cannot be created, so this API call must fail
        restNotificationMockMvc
            .perform(
                post(ENTITY_API_URL)
                    .with(csrf())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(notification))
            )
            .andExpect(status().isBadRequest());

        // Validate the Notification in the database
        List<Notification> notificationList = notificationRepository.findAll();
        assertThat(notificationList).hasSize(databaseSizeBeforeCreate);
    }

    @Test
    @Transactional
    void getAllNotifications() throws Exception {
        // Initialize the database
        notificationRepository.saveAndFlush(notification);

        // Get all the notificationList
        restNotificationMockMvc
            .perform(get(ENTITY_API_URL + "?sort=id,desc"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(notification.getId().intValue())))
            .andExpect(jsonPath("$.[*].notificationId").value(hasItem(DEFAULT_NOTIFICATION_ID.intValue())))
            .andExpect(jsonPath("$.[*].notifySeq").value(hasItem(DEFAULT_NOTIFY_SEQ.intValue())))
            .andExpect(jsonPath("$.[*].notifyType").value(hasItem(DEFAULT_NOTIFY_TYPE)))
            .andExpect(jsonPath("$.[*].notifyTo").value(hasItem(DEFAULT_NOTIFY_TO)))
            .andExpect(jsonPath("$.[*].notifyBy").value(hasItem(DEFAULT_NOTIFY_BY)));
    }

