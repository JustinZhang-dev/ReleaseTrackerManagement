    @Test
    @Transactional
    void partialUpdateNotificationWithPatch() throws Exception {
        // Initialize the database
        notificationRepository.saveAndFlush(notification);

        int databaseSizeBeforeUpdate = notificationRepository.findAll().size();

        // Update the notification using partial update
        Notification partialUpdatedNotification = new Notification();
        partialUpdatedNotification.setId(notification.getId());

        partialUpdatedNotification.notifyTo(UPDATED_NOTIFY_TO);

        restNotificationMockMvc
            .perform(
                patch(ENTITY_API_URL_ID, partialUpdatedNotification.getId())
                    .with(csrf())
                    .contentType("application/merge-patch+json")
                    .content(TestUtil.convertObjectToJsonBytes(partialUpdatedNotification))
            )
            .andExpect(status().isOk());

        // Validate the Notification in the database
        List<Notification> notificationList = notificationRepository.findAll();
        assertThat(notificationList).hasSize(databaseSizeBeforeUpdate);
        Notification testNotification = notificationList.get(notificationList.size() - 1);
        assertThat(testNotification.getNotificationId()).isEqualTo(DEFAULT_NOTIFICATION_ID);
        assertThat(testNotification.getNotifySeq()).isEqualTo(DEFAULT_NOTIFY_SEQ);
        assertThat(testNotification.getNotifyType()).isEqualTo(DEFAULT_NOTIFY_TYPE);
        assertThat(testNotification.getNotifyTo()).isEqualTo(UPDATED_NOTIFY_TO);
        assertThat(testNotification.getNotifyBy()).isEqualTo(DEFAULT_NOTIFY_BY);
    }

